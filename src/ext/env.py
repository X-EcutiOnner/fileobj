import fileobj.env

def get_text(co, fo, args):
    envs = list(fileobj.env.iter_defined_env())
    if not len(envs):
        return "No env"
    n = max([len(x[0]) for x in envs])
    f = "{{0:<{0}}} \"{{1}}\"".format(n)
    l = []
    for x in envs:
        l.append(f.format(*x))
    return l
